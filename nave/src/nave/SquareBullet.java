package nave;

public class SquareBullet {
	private int width;
	private int height;
	private int speed;
	
	
	public SquareBullet() {
		width=10;
		height=10;
		speed=3;
	}
	
	public int getWidth() {return width;}
	public int getHeight() {return height;}
	public int getSpeed() {return speed;}
}
